    private void Btn_EmitirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_EmitirActionPerformed
        // TODO add your handling code here
        mObjFactura.setFech(Dat_Fech.getDate());// Asigna la fecha a ala factura 
        try {
            mObjFactura.setPersona(mObjPerSel); // Asigna la persona a la factura
        } catch (InputException ex) {
                JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
                return;
        }
        
        FrmDi_ForPago FrmFP = new FrmDi_ForPago(mFrmCon, mObjFactura);
        if (FrmFP.getStatus() == FrmDi_ForPago.RET_OK){
            try {
                
                this.altaFactura(FrmFP.getPresupuesto());
                
                //                Rep_Class_IniciarReporte.imprimirFactura(mObjFactura);
                
            } catch (InputException ex) {
                JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
                return;
            }
            this.Cancelar();
            
        }
    }//GEN-LAST:event_Btn_EmitirActionPerformed

