    private void CargarNuFac (LogN_ClassAb_Factura xObjFac){
        if (mObjFactura == xObjFac) return;

        mObjFactura = xObjFac;
        TxtF_NumF.setText("" + xObjFac.getNum());
        Dat_Fech.setDate(xObjFac.getFech());

        if (modeljTabLinFac == null){
            modeljTabLinFac = new ModeljTabLinFac(xObjFac.getmColLinF());
            Tab_LineaFact.setModel(modeljTabLinFac);
        } else {
            modeljTabLinFac.setDatos(xObjFac.getmColLinF());
        }

        xObjFac.addObserver(this);
        xObjFac.addObserver(modeljTabLinFac);

        cargarTotales();
    }

    private void cargarTotales (){
        Lbl_IVA.setText("" + mformatoNum.format(mObjFactura.getResIva1()));
        Lbl_SubTotal.setText("" + mformatoNum.format(mObjFactura.getSubTot()));
        Lbl_Total.setText("" + mformatoNum.format(mObjFactura.getTotFin()));
    }

    public void update(Observable o, Object arg) {
       if (mObjFactura == o){
           cargarTotales();
       }
    }

    private void Cancelar() {
        mObjFactura.deleteObserver(this);
        mObjFactura.deleteObserver(modeljTabLinFac);
        CargarNuFac(mObjFachada.NuevaFachVen()); //   Modificar Producto
        eliminarDataPer();
        eliminarDatosPerSelec();
    }



    

    private void bajaLF (){
        int Pos = Tab_LineaFact.getSelectedRow();
        if (Pos == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una línea de factura a dar de baja");
            return;
        }

        mObjFactura.bajaLineaFach(modeljTabLinFac.getLinFac(Pos));

    }
    
