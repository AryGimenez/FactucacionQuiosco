    private void selecPer (){
        LogN_ClassAb_Persona xObjPer = selexioPersona();
        if (xObjPer == null) eliminarDatosPerSelec();
        else cargarPer(xObjPer);
    }
    
    
    
    private void cargarPer (LogN_ClassAb_Persona xObjPersona){
        TxtF_CodPer.setValue(xObjPersona.getmNum());
        Lbl_RasSos.setText(" " +xObjPersona.getmRasSos());
        Lbl_Ruc.setText(" " + xObjPersona.getmRut());
        Lbl_Dir.setText(" " + xObjPersona.getmDir());
        
        try{
        Lbl_Cel.setText(" " + xObjPersona.getmColCel().get(0).getNom());  
        }catch(java.lang.IndexOutOfBoundsException ex){
          Lbl_Cel.setText("");
        }

        try{
        Lbl_Tel.setText(" " +xObjPersona.getmColTel().get(0).getNom());  
        }catch(java.lang.IndexOutOfBoundsException ex){
          Lbl_Tel.setText("");
        }

    }
    
    public void eliminarDataPer (){
        TxtF_Cel.setText("");
        TxtF_Rur.setText("");
        TxtF_Tel.setText("");
        Txt_Dir.setText("");
        Txt_RasSos.setText("");
    }
    
    public void eliminarDatosPerSelec(){
        Lbl_RasSos.setText("");
        Lbl_Ruc.setText("");
        Lbl_Dir.setText("");
        Lbl_Tel.setText("");
        Lbl_Cel.setText("");
        TxtF_CodPer.setText("");
    }
    
    private void cargarCodPer (String CodPer){        
        CodPer = CodPer.trim();
        if (CodPer.equals("")){
            eliminarDatosPerSelec();
            mObjPerSel = null;
        }else{
            Integer CodP;
            try{
                CodP = Integer.parseInt(CodPer);
                LogN_ClassAb_Persona ObjPer = this.getPer(CodP.intValue());
                if(ObjPer == null) {
                    JOptionPane.showMessageDialog(TxtF_CodPer, "No se pudo encontrar la persona, compruebe código");
                    TxtF_CodPer.setText("");
                }else{
                    mObjPerSel =  ObjPer;
                    cargarPer(ObjPer);
                }

            }catch(java.lang.NumberFormatException ex){
                JOptionPane.showMessageDialog(TxtF_CodPer, "El dato ingresado no es un dato numerico");
            }
            
        }
        
    }
