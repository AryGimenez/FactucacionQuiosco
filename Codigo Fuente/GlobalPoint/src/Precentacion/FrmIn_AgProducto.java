    private void AltaMov() {
        try {
            Dom_Class_ProductoComp ObjProdC = this.getProductoCom();
            FrmDi_Movimiento ObjFrmMov = new FrmDi_Movimiento(mObjFrmCon, ObjProdC.generarMovimiento("", "", "", 0, true));
            ObjFrmMov.setVisible(true);
            if (ObjFrmMov.getReturnStatus() == FrmDi_Movimiento.RET_OK){
                mObjMov = ObjFrmMov.getMov();
                Btn_AgStok.setText("" + mObjMov.getCanProd());
            }
        } catch (InputException ex) {
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
        }
    }
    
    public float calProGan (float xInsumo, float xPreVen){
        if (xInsumo == 0 || xPreVen == 0) return 0;
        return (xPreVen/xInsumo - 1)*100;
    }
    
    public float calPreVen (float xInsum, float xPrsGan){
        
        return xInsum*(xPrsGan/100+1);
    }
    
    







