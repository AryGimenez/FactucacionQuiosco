    protected Dom_Class_ProductoComp SubProd (Dom_Class_ProductoComp xObjProd) throws InputException{

        if (!Che_SubP.isSelected()) return xObjProd;
        xObjProd.validar();

        Dom_Class_ProductoComp ObjProdC = new Dom_Class_ProductoComp(xObjProd);
        FrmDi_AgregarSubProd ObjFrmSub = new FrmDi_AgregarSubProd(mObjFrmCon, ObjProdC);
        ObjFrmSub.setVisible(true);

        if (ObjFrmSub.getReturnStatus() == FrmDi_AgregarSubProd.RET_OK) xObjProd = ObjFrmSub.getProducto();
        
        return xObjProd;

    }

