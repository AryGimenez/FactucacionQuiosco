    private void limNot (){
        Txt_NotNom.setText("");
        TxtA_NotDetalle.setText("");
    }
    
    protected void cargarNotific (){
        mObjObservado.deleteObserver(mObjMNottificacion);
        mObjMNottificacion = new ModeljTabNotificacion(new ArrayList<Dom_Class_Notificacion>());
        mObjObservado.addObserver(mObjMNottificacion);
        Tab_Not.setModel(mObjMNottificacion);
    }
    
    private void agregarNot(){
        Dom_Class_Notificacion xObjNot = getNotifica();
        try {
            xObjNot.validar();
            mObjObservado.Notifiar(new LogN_Class_Accion<Dom_Class_Notificacion>(LogN_Class_Accion.Agreger,xObjNot));
            this.limNot();
        } catch (InputException ex) {
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
        }

    }
    
    private void bajaNot (Dom_Class_Notificacion xObjNot){
        if (xObjNot == null) JOptionPane.showMessageDialog(this, "No selexiono ninguna Notificacion a dar de baja");
        mObjObservado.Notifiar(new LogN_Class_Accion<Dom_Class_Notificacion>(LogN_Class_Accion.Eliminar,xObjNot));
    }
    
    private Dom_Class_Notificacion getNotifica(){
        return new Dom_Class_Notificacion(Txt_NotNom.getText(), TxtA_NotDetalle.getText(), Cal_NotFech.getDate());
    }

    public Dom_Class_ProductoComp getProductoCom () throws InputException {
         Dom_Class_Categoria ObjCat = null;
         Dom_Class_IVA ObjIva = (Dom_Class_IVA) ComB_ConIVA.getSelectedItem();
         try {
             ObjCat = mObjFachada.RetornarOCrearCategoria(ComBAu_Categoria.getSelectedItem().toString());
        } catch (InputException ex) {
            throw  new InputException (ex.getLocalizedMessage());
        }catch (NullPointerException ex2){
            throw new InputException ("No se pudo crear la categoria");
        }

        int CodProd = Integer.parseInt(Lbl_CodIn.getText());


        Number PreCos = (Number) TxtF_PreCos.getValue();
        Number PreVen = (Number) TxtF_PreVen.getValue();
        Number StokMin = (Number) TxtF_StokMin.getValue();
        

        return new Dom_Class_ProductoComp(CodProd,Txt_Nom.getText(),TxtA_Des1.getText(),
                    TxtF_CodBarr.getText(), PreCos.floatValue(), PreVen.floatValue(),
                StokMin.floatValue(),ObjCat,ComB_TipMed.getSelectedIndex(),
                ObjIva);
    }
