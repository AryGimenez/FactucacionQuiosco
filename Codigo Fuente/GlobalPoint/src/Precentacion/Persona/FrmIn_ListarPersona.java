    private void Btn_ModLimSaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ModLimSaldoActionPerformed
        // TODO add your handling code here:
        float LimAcree = ((Number) TxtF_LimAcre.getValue()).floatValue(), 
                LimDeud = ((Number) TxtF_LimDeu.getValue()).floatValue();
        
        if (mObjSalSel.getmLimAceedor() != LimAcree){
            try {
                modificarSaldo(true, LimAcree);
            } catch (InputException ex) {
                JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
                TxtF_LimAcre.setValue(mObjSalSel.getmLimAceedor());
            }
        } else if (mObjSalSel.getmLimDeudor() != LimDeud){
            try {
                modificarSaldo(false, LimDeud);
            } catch (InputException ex) {
                JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
                TxtF_LimDeu.setValue(mObjSalSel.getmLimDeudor());
            }
        }else{
            JOptionPane.showMessageDialog(this, "No a modificado los limites del saldo");
        }
        
    }//GEN-LAST:event_Btn_ModLimSaldoActionPerformed

