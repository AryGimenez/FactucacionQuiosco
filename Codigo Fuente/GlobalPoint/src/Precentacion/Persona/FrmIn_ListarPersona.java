        private void cargarContacto (LogN_ClassAb_Persona xObjPersona, int TipCon){
        ArrayList ColContacto = null;
        
        switch (TipCon){
            case 0: ColContacto = xObjPersona.getmColTel(); break;
            case 1: ColContacto = xObjPersona.getmColCel(); break;
            case 2: ColContacto = xObjPersona.getmColFax(); break;
            case 3: ColContacto = xObjPersona.getmColMail(); break;
            default: return;
        }
        Lis_Contacto.setModel(new PersonalListModel(ColContacto, PersonalComboBoxMedel.Sin_Orden, null));
        }
        
        private void modificarContacto (LogN_ClassAb_Persona xObjPersona ,int xTipCon){
             if (xObjPersona == null) {
                 JOptionPane.showMessageDialog(this, "Debe Seleccionar alguna persona para modificar sus contacto");
                 return;
             }
             FrmDi_Contacto FrmCreXNom;
             HashMap <Integer, LogN_ClassAb_Contacto> MapContacto = new HashMap<Integer, LogN_ClassAb_Contacto>();
             ArrayList ColCon = new ArrayList();

             switch (xTipCon){
                case 0: //Opcion Telefono
                    ColCon = xObjPersona.getmColTel();
                    FrmCreXNom = new FrmDi_Contacto(mFrmCon,"Telefono",ColCon ) {
                        @Override
                        protected LogN_ClassAb_Contacto cargarContacto() throws InputException {
                            Dom_Class_Tel ObjTel = new Dom_Class_Tel
                            (TxtF_Modificar.getText(), TxtA_Detalle.getText());
                            ObjTel.validar();
                            return ObjTel;
                        }
                    } ;

                break;
                case 1: //Celular
                    ColCon = xObjPersona.getmColCel();
                    FrmCreXNom = new FrmDi_Contacto(mFrmCon, "Celular", ColCon) {
                        @Override
                        protected LogN_ClassAb_Contacto cargarContacto() throws InputException {
                            Dom_Class_Cel ObjCel = new Dom_Class_Cel
                            (TxtF_Modificar.getText(), TxtA_Detalle.getText());
                            ObjCel.validar();
                            return ObjCel;
                        }
                    } ;
                break;
                case 2: 
                    ColCon = xObjPersona.getmColFax();
                    FrmCreXNom = new FrmDi_Contacto(mFrmCon, "Fax", ColCon) {
                        @Override
                        protected LogN_ClassAb_Contacto cargarContacto() throws InputException {
                            Dom_Class_Fax ObjFax = new Dom_Class_Fax
                            (TxtF_Modificar.getText(), TxtA_Detalle.getText());
                            ObjFax.validar();
                            return ObjFax;
                        }
                    } ;
                break;
                case 3: 
                    ColCon = xObjPersona.getmColMail();
                    FrmCreXNom = new FrmDi_Contacto(mFrmCon, "Mail", ColCon) {
                        @Override
                        protected LogN_ClassAb_Contacto cargarContacto() throws InputException {
                            Dom_Class_Mail ObjMail = new Dom_Class_Mail
                            (TxtF_Modificar.getText(), TxtA_Detalle.getText());
                            ObjMail.validar();
                            return ObjMail;
                        }
                    } ;
                break;
                default: return;
             }
            FrmCreXNom.setVisible(true);

            for (Object o : ColCon){
                LogN_ClassAb_Contacto ObjContacto = (LogN_ClassAb_Contacto) o;
                MapContacto.put(xObjPersona.getmNum(), ObjContacto);  // Creo que esta mal

            }

            if(FrmDi_CreaPorNom.RET_OK != FrmCreXNom.getReturnStatus()) return;// Pregunto si confirmo los cambios

            for (LogN_Class_Accion<LogN_ClassAb_Contacto> ObjAcc  : FrmCreXNom.getAccion()){
                LogN_ClassAb_Contacto ObjCon = ObjAcc.getmAccionar();
                switch (ObjAcc.mAccionActual) {
                    case LogN_Class_Accion.Agreger:
                        try {
                            mObjFachada.altaContacto((LogN_ClassAb_PerExterna)xObjPersona, ObjCon);
                        } catch (InputException ex) {
                            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
                        }
                    break;
                    case LogN_Class_Accion.Modificar:

                        LogN_ClassAb_Contacto ObjConAn = MapContacto.get(ObjCon.getNum());


                        try {
                            mObjFachada.modifContacto(ObjConAn, ObjCon);
                        } catch (InputException ex) {
                            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
                        }

                    break;
                    case LogN_Class_Accion.Eliminar:
                        try {
                            mObjFachada.bajaContacto((LogN_ClassAb_PerExterna)xObjPersona, ObjCon);
                        } catch (InputException ex) {
                            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
                        }


                    break;
                }// Fin switch
            } //Fin for

            this.cargarContacto(xObjPersona, xTipCon);
        
        } // Fin modificarContacto
    
