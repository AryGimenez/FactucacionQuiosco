        private void ModificarDir(LogN_ClassAb_Persona xObjPer) {

             if ( xObjPer == null) {
                 JOptionPane.showMessageDialog(this, "Debe Seleccionar alguna persona para modificar su direccion");
                 return;
             }
             String Dir = TxtA_Dir.getText();
             if (xObjPer.getmDir().equals(Dir)){
                 JOptionPane.showMessageDialog(this, "No a modificado la direccion");
                 return;
             }

             LogN_ClassAb_Persona ObjPerNu = xObjPer.duplicar();

             try {
                mObjFachada.ModPersonaEx((LogN_ClassAb_PerExterna)xObjPer,(LogN_ClassAb_PerExterna) ObjPerNu);
            } catch (InputException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        
