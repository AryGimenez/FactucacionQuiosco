        private void listarSaldo (LogN_ClassAb_Persona xObjPersona){
            ArrayList <Dom_Class_Saldo> ColSaldo = xObjPersona.getmColSaldo();
            int CanSal = ColSaldo.size();
            ComB_Saldo.setModel(new DefaultComboBoxModel(ColSaldo.toArray()));

            Lbl_CanSal.setText("" + CanSal);
            if(CanSal > 0) ComB_Saldo.setSelectedIndex(0);
            if(ColSaldo.size() > 0) cargarSaldo(ColSaldo.get(0));
            
        }
        private void cargarSaldo (Dom_Class_Saldo xObjSaldo){
          mObjSalSel = xObjSaldo;
          TxtF_LimAcre.setValue(xObjSaldo.getmLimAceedor());
          TxtF_LimDeu.setValue(xObjSaldo.getmLimDeudor());
        }
        
        private void modificarSaldo (boolean xAcreedor, float xValLim) throws InputException{
            Dom_Class_Saldo ObjSaldo = mObjSalSel.duplicar();
            if (xAcreedor){
                if (xValLim == ObjSaldo.getmLimAceedor()) return;
                ObjSaldo.setmLimAceedor(xValLim);
            } else {
                if (xValLim == ObjSaldo.getmLimDeudor()) return;
                ObjSaldo.setmLimDeudor(xValLim);
            }

            mObjFachada.modifSaldo(mObjPerSel, ObjSaldo);
 

        }
